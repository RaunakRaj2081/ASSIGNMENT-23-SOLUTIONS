// Write a C++ program to swap values of two int variables without using third variable

#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout << "Enter two numbers :"<<endl;
    cin>>a>>b;
    cout<<"Before swapping "<<endl;
    cout<<"a = "<<a<<endl;
    cout <<"b = "<<b<<endl;
    cout<< "After swapping "<<endl;
    cout<<"a = "<<b<<endl;
    cout <<"b = "<<a<<endl;
    return 0;

}